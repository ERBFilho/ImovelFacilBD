package modelo;

public class Locador {
    private int Cod_Locador;
    private String Nome_Locador;
    private String Endereco_Locador;
    private String Bairro_Locador;
    private String Cidade_Locador;
    private String Estado_Locador;
    private String Celular_Locador;
    private String Telefone_Locador;
    private String Email_Locador;
    private String Senha_Locador; 
    
    public int getCod_Locador() {
        return Cod_Locador;
    }

    public void setCod_Locador(int Cod_Locador) {
        this.Cod_Locador = Cod_Locador;
    }
    
    public String getNome_Locador() {
        return Nome_Locador;
    }

    public void setNome_Locador(String Nome_Locador) {
        this.Nome_Locador = Nome_Locador;
    }

    public String getEndereco_Locador() {
        return Endereco_Locador;
    }

    public void setEndereco_Locador(String Endereco_Locador) {
        this.Endereco_Locador = Endereco_Locador;
    }

    public String getBairro_Locador() {
        return Bairro_Locador;
    }

    public void setBairro_Locador(String Bairro_Locador) {
        this.Bairro_Locador = Bairro_Locador;
    }

    public String getCidade_Locador() {
        return Cidade_Locador;
    }

    public void setCidade_Locador(String Cidade_Locador) {
        this.Cidade_Locador = Cidade_Locador;
    }

    public String getEstado_Locador() {
        return Estado_Locador;
    }

    public void setEstado_Locador(String Estado_Locador) {
        this.Estado_Locador = Estado_Locador;
    }

    public String getCelular_Locador() {
        return Celular_Locador;
    }

    public void setCelular_Locador(String Celular_Locador) {
        this.Celular_Locador = Celular_Locador;
    }

    public String getTelefone_Locador() {
        return Telefone_Locador;
    }

    public void setTelefone_Locador(String Telefone_Locador) {
        this.Telefone_Locador = Telefone_Locador;
    }

    public String getEmail_Locador() {
        return Email_Locador;
    }

    public void setEmail_Locador(String Email_Locador) {
        this.Email_Locador = Email_Locador;
    }

    public String getSenha_Locador() {
        return Senha_Locador;
    }

    public void setSenha_Locador(String Senha_Locador) {
        this.Senha_Locador = Senha_Locador;
    }
}
